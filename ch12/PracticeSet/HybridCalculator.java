package calsi;

public class HybridCalculator{
    public static void main(String[] args){
        int a=4, b=5;
        System.out.println("a+b = "+(a+b));
        System.out.println("a-b = "+(a-b));
        System.out.println("exp(a*b) = "+Math.exp(a*b));
        System.out.println("sqrt(a/b) = "+Math.sqrt(a/b));
    }
}